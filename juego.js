        let currentQuestions = [];
        let currentQuestion = 0;
        let lives = 4;
        let score = 0;
        let consecutiveCorrect = 0;
        let timeLeft = 300; // 5 minutos en segundos
        let questionTimer = 15;
        let gameTimer;
        let questionInterval;
        let wrongAnswers = [];

        function shuffleQuestions() {
            currentQuestions = [...questions].sort(() => Math.random() - 0.5);
        }

        function startGame() {
            const playerName = document.getElementById('player-name').value;
            if (!playerName) {
                alert('Por favor ingresa tu nombre');
                return;
            }

            document.getElementById('welcome-screen').style.display = 'none';
            document.getElementById('game-screen').style.display = 'block';
            
            shuffleQuestions();
            startTimers();
            showQuestion();
        }

        function startTimers() {
            gameTimer = setInterval(() => {
                timeLeft--;
                updateTimer();
                if (timeLeft <= 60) {
                    document.getElementById('timer').classList.add('warning');
                }
                if (timeLeft <= 0) {
                    endGame();
                }
            }, 1000);

            startQuestionTimer();
        }

        function startQuestionTimer() {
            questionTimer = 15;
            updateQuestionTimer();
            if (questionInterval) clearInterval(questionInterval);
            questionInterval = setInterval(() => {
                questionTimer--;
                updateQuestionTimer();
                if (questionTimer <= 0) {
                    handleAnswer(-1);
                }
            }, 1000);
        }

        function updateQuestionTimer() {
            const timerElement = document.getElementById('question-time');
            timerElement.textContent = questionTimer;
            if (questionTimer <= 5) {
                timerElement.classList.add('warning');
            } else {
                timerElement.classList.remove('warning');
            }
        }

        function updateTimer() {
            const minutes = Math.floor(timeLeft / 60);
            const seconds = timeLeft % 60;
            document.getElementById('timer').textContent = 
                `${minutes}:${seconds.toString().padStart(2, '0')}`;
        }

        function showQuestion() {
            if (currentQuestion >= currentQuestions.length) {
                endGame();
                return;
            }

            const question = currentQuestions[currentQuestion];
            document.getElementById('question').textContent = question.question;
            
            const optionsContainer = document.getElementById('options');
            optionsContainer.innerHTML = '';
            
            question.options.forEach((option, index) => {
                const button = document.createElement('button');
                button.textContent = option;
                button.onclick = () => handleAnswer(index);
                optionsContainer.appendChild(button);
            });

            startQuestionTimer();
        }

        function handleAnswer(selectedIndex) {
            clearInterval(questionInterval);
            const question = currentQuestions[currentQuestion];
            
            if (selectedIndex === question.correct) {
                score += 100;
                consecutiveCorrect++;
                if (consecutiveCorrect === 5) {
                    lives++;
                    consecutiveCorrect = 0;
                    alert('¡Ganaste una vida extra!');
                }
            } else {
                lives -= 0.5;
                consecutiveCorrect = 0;
                wrongAnswers.push({
                    question: question.question,
                    correctAnswer: question.options[question.correct],
                    explanation: question.explanation
                });
            }

            document.getElementById('lives').textContent = lives;
            document.getElementById('score').textContent = score;

            if (lives <= 0) {
                endGame();
                return;
            }

            currentQuestion++;
            showQuestion();
        }

        function endGame() {
            clearInterval(gameTimer);
            clearInterval(questionInterval);

            document.getElementById('game-screen').style.display = 'none';
            document.getElementById('results-screen').style.display = 'block';

            const timeSpent = 300 - timeLeft;
            const minutes = Math.floor(timeSpent / 60);
            const seconds = timeSpent % 60;

            document.getElementById('final-stats').innerHTML = `
                <h3>Estadísticas finales:</h3>
                <p>Puntuación final: ${score}</p>
                <p>Tiempo usado: ${minutes}:${seconds.toString().padStart(2, '0')}</p>
                <p>Preguntas correctas: ${score/100} de ${currentQuestion}</p>
            `;
     const wrongAnswersContainer = document.getElementById('wrong-answers');
            if (wrongAnswers.length > 0) {
                wrongAnswersContainer.innerHTML = '<h3>Respuestas incorrectas:</h3>';
                wrongAnswers.forEach(wrong => {
                    wrongAnswersContainer.innerHTML += `
                        <div class="wrong-answer">
                            <p><strong>Pregunta:</strong> ${wrong.question}</p>
                            <p><strong>Respuesta correcta:</strong> ${wrong.correctAnswer}</p>
                            <p><strong>Explicación:</strong> ${wrong.explanation}</p>
                        </div>
                    `;
                });
            }
        }

        function restartGame() {
            lives = 4;
            score = 0;
            currentQuestion = 0;
            consecutiveCorrect = 0;
            timeLeft = 300;
            wrongAnswers = [];
            document.getElementById('results-screen').style.display = 'none';
            document.getElementById('welcome-screen').style.display = 'block';
            document.getElementById('timer').classList.remove('warning');
        }
